static void print_logo_narrow(void) {

    /* wpm counter */
    uint8_t n = get_current_wpm();
    char    wpm_str[4];
    oled_set_cursor(0, 14);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);

    oled_set_cursor(0, 15);
    oled_write(" wpm", false);
}

#include "string.h"
static void print_status_narrow(void) {
    /* Print current mode */
    oled_set_cursor(0, 0);

    oled_write_raw_P(windows_logo, sizeof(windows_logo));

    oled_set_cursor(0, 3);

    // Get the current WPM value
    uint16_t current_wpm = get_current_wpm();

    // Create a buffer to store the WPM value as a string
    char wpm_str[10];

    // Clear the wpm_str buffer
    memset(wpm_str, 0, sizeof(wpm_str));

    // Convert the WPM value to a string using itoa()
    itoa(current_wpm, wpm_str, 10);

    // Pad the output with spaces to ensure proper formatting
    int len = strlen(wpm_str);
    for (int i = len; i < 3; i++) {
        wpm_str[i] = ' ';
    }

    // Write "WPM:" to the OLED screen
    oled_write("WPM:", false);

    // Write a line break to the OLED screen
    oled_write("\n", false);

    // Write the actual WPM value to the OLED screen
    oled_write(wpm_str, false);


    oled_set_cursor(0, 7);

    /* Print current layer */
    oled_write("LAYER", false);

    oled_set_cursor(0, 8);

    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write("Base ", false);
            break;
        case _GAMING:
            oled_write("Func", false);
            break;
        case _RAISE:
            oled_write("Raise", false);
            break;
        case _LOWER:
            oled_write("Lower", false);
            break;
        case _ADJUST:
            oled_write("Adj  ", false);
            break;
        default:
            oled_write("Undef", false);
    }

    /* caps lock */
    oled_set_cursor(0, 10);
    oled_write("CPSLK", led_usb_state.caps_lock);

    /* KEYBOARD PET RENDER START */

    render_luna(0, 13);

    /* KEYBOARD PET RENDER END */
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) { return OLED_ROTATION_270; }

bool oled_task_user(void) {
    /* KEYBOARD PET VARIABLES START */

    current_wpm   = get_current_wpm();
    led_usb_state = host_keyboard_led_state();

    /* KEYBOARD PET VARIABLES END */

    if (is_keyboard_master()) {
        print_status_narrow();
    } else {
        print_logo_narrow();
    }
    return false;
}

